#Opereções matemáticas no Python

num1 = 10
num2 = 5

#SOMA
print(f'A soma é {num1 + num2}')

#SUBTRAÇÃO
print(f"A subtração é {num1 - num2}")

#MULTIPLICAÇÃO
print(f'A multiplicação é {num1*num2}')

#DIVISÃO (REAL)
print(f'A divisaõ é {num1/num2}')

x = 3
y = 2

#DIVISÃO (PARTE INTEIRA)
print('A divisaõ inteira é {}'.format(x//y))

#EXPONENCIAL
print('Um nuúmero elevado ao quardado é {}'.format(num1**2))

#RESTO DE DIVISÃO
print('O resto da divisaõ de {} por {} é {}'.format(num1, num2, num1%num2))