#Exemplos de saída de dados (exibição na tela)

#Exemplo 1: exibir uma mensagem de texto na tela
print(f'Primeira mensagem de texto em Python!')

#Exemplo 2: definir uma variavel numérica e exibir seu conteúdo
numero_inteiro = 8
print(f'o número é: {numero_inteiro}')

#Exibir o tipo de dado da variável
print(type(numero_inteiro))

#Exemplo 3: definir uma variável numérica real e exibir e seu conteúdo
salario = 27000.00
print(type(salario))
print(f'seu saldo é de {salario}')

# Exemplo 4: exibir uma variável texto e exibir seu conteúdo
nome_pessoa = 'Lucas Borges'
print(type(nome_pessoa))
print(nome_pessoa)

#Exemplo 5: exibir um mensagem de texto junto com contéudo de uma variavel
print('Meu nome é {}'.format(nome_pessoa))

#outra forma de exibir texto com conteúdo de variavel
print(f'O meu salário é de R${salario}')